"""
Comparison operators in Python are used to compare two values or variables.
They evaluate the relationship between two operands and
return a Boolean result: either True or False.
"""

# Examples of Comparison Operators
# Equal to (==)
# Checks if two values are equal.
a = 10
b = 20
print(a == b)  # Output: False (10 is not equal to 20)
print(a == 10)  # Output: True (10 is equal to 10)

# Not equal to (!=)
# Checks if two values are not equal.
a = 10
b = 20
print(a != b)  # Output: True (10 is not equal to 20)

# Greater than (>)
# Checks if the left operand is greater than the right operand.
a = 15
b = 10
print(a > b)  # Output: True (15 is greater than 10)
print(b > a)  # Output: False (10 is not greater than 15)

# Less than (<)
# Checks if the left operand is less than the right operand.
a = 5
b = 15
print(a < b)  # Output: True (5 is less than 15)

# Greater than or equal to (>=)
# Checks if the left operand is greater than or equal to the right operand.
a = 10
b = 10
print(a >= b)  # Output: True (10 is equal to 10)
print(a >= 5)  # Output: True (10 is greater than 5)

# Less than or equal to (<=)
# Checks if the left operand is less than or equal to the right operand.
a = 5
b = 10
print(a <= b)  # Output: True (5 is less than 10)
print(b <= 5)  # Output: False (10 is not less than or equal to 5)











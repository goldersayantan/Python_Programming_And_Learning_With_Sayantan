"""
Arithmetic operators in Python are used to perform basic mathematical operations
such as addition, subtraction, multiplication, division, etc.
These operators work with both integers and floating-point numbers, and in some cases,
they can also operate on complex numbers or other numeric types.
"""

# Examples of Arithmetic Operators
# Addition
a = 10
b = 5
result = a + b
print(result)  # Output: 15

# Subtraction
a = 10
b = 5
result = a - b
print(result)  # Output: 5

# Multiplication
a = 10
b = 5
result = a * b
print(result)  # Output: 50

# Division
a = 15
b = 2
result = a / b
print(result)  # Output: 7.5

# Floor Division
a = 15
b = 2
result = a // b
print(result)  # Output: 7

# Modulus
a = 15
b = 4
result = a % b
print(result)  # Output: 3

# Exponentiation
a = 2
b = 3
result = a ** b
print(result)  # Output: 8
